// -----------------------------------------------------------------------------
// UTILITY TYPES
// -----------------------------------------------------------------------------

/**
 * Extract the ID type from an entity type
 */
export type IdOf<T extends LKGCEntity> = T["id"];

/**
 * Make selected fields optional
 */
export type PartialBy<T, K extends keyof T> = Omit<T, K> & Partial<Pick<T, K>>;

/**
 * Make all fields except selected ones optional
 */
export type RequireOnly<T, K extends keyof T> = Partial<T> & Pick<T, K>;

/**
 * Deep readonly type
 */
export type DeepReadonly<T> = T extends (infer U)[]
  ? readonly DeepReadonly<U>[]
  : T extends object
    ? { readonly [K in keyof T]: DeepReadonly<T[K]> }
    : T;

/**
 * Numeric range type (for validation documentation)
 */
export interface NumericRange {
  readonly min: number;
  readonly max: number;
  readonly step?: number;
}

/**
 * Normalized value in [0, 1]
 */
export type NormalizedValue = Brand<number, "NormalizedValue">;

/**
 * Score that can be negative (e.g., -1 to 1)
 */
export type BipolarScore = Brand<number, "BipolarScore">;

/**
 * Percentage (0-100)
 */
export type Percentage = Brand<number, "Percentage">;
